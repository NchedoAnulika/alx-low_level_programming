#ifndef DOG_H
#define DOG_H
/**
 * struct dog - Represents information about a dog.
 * @name: The name of the dog.
 * @age: The age of the dog.
 * @owner: The name of the dog's owner.
 *
 * Return: Always 0 (indicating successful execution).
 */
struct dog
{
	char *name;
	float age;
	char *owner;
};

#endif
